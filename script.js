async function capturarFoto() {
    try {
        const context = canvas.getContext('2d');

        // Definir dimens√µes do canvas baseado no v√≠deo
        canvas.width = video.videoWidth;
        canvas.height = video.videoHeight;

        // Desenhar o v√≠deo no canvas
        context.drawImage(video, 0, 0, canvas.width, canvas.height);

        // Adicionar overlay escuro sutil para melhor contraste
        const gradient = context.createLinearGradient(0, 0, 0, canvas.height);
        gradient.addColorStop(0, 'rgba(0, 0, 0, 0.1)');
        gradient.addColorStop(1, 'rgba(0, 0, 0, 0.3)');
        context.fillStyle = gradient;
        context.fillRect(0, 0, canvas.width, canvas.height);

        // Carregar e desenhar o bras√£o de forma segura
        if (casaAtual) {
            const brasaoCanvas = await imagemParaCanvas(casasTech[casaAtual].img);

            if (brasaoCanvas) {
                // Calcular posi√ß√£o e tamanho do bras√£o (maior e mais centralizado)
                const brasaoSize = Math.min(canvas.width * 0.35, canvas.height * 0.35); // Aumentado de 0.2 para 0.35
                const brasaoX = canvas.width - brasaoSize - 30;
                const brasaoY = 30;

                // Adicionar efeito de sombra ao bras√£o
                context.shadowColor = 'rgba(0, 0, 0, 0.5)';
                context.shadowBlur = 20;
                context.shadowOffsetX = 5;
                context.shadowOffsetY = 5;

                // Desenhar c√≠rculo de fundo com gradiente
                const centerX = brasaoX + brasaoSize / 2;
                const centerY = brasaoY + brasaoSize / 2;
                const radius = brasaoSize / 2 + 10;

                const brasaoGradient = context.createRadialGradient(centerX, centerY, 0, centerX, centerY, radius);
                brasaoGradient.addColorStop(0, 'rgba(255, 255, 255, 0.9)');
                brasaoGradient.addColorStop(0.7, 'rgba(255, 255, 255, 0.6)');
                brasaoGradient.addColorStop(1, 'rgba(255, 255, 255, 0.2)');

                context.fillStyle = brasaoGradient;
                context.beginPath();
                context.arc(centerX, centerY, radius, 0, 2 * Math.PI);
                context.fill();

                // Desenhar o bras√£o redimensionado
                context.drawImage(brasaoCanvas, brasaoX, brasaoY, brasaoSize, brasaoSize);

                // Resetar sombra
                context.shadowColor = 'transparent';
                context.shadowBlur = 0;
                context.shadowOffsetX = 0;
                context.shadowOffsetY = 0;

                // Adicionar brilho ao redor do bras√£o
                context.strokeStyle = 'rgba(255, 255, 255, 0.8)';
                context.lineWidth = 3;
                context.beginPath();
                context.arc(centerX, centerY, radius - 5, 0, 2 * Math.PI);
                context.stroke();
            }
        }

        // Adicionar logo do Senac no canto inferior esquerdo
        await adicionarLogoSenac(context);

        // Adicionar texto decorativo
        await adicionarTextoDecorativo(context);

        // Adicionar efeitos de part√≠culas/estrelas
        adicionarEfeitosVisuais(context);

        // Converter para imagem e mostrar preview
        const dataURL = canvas.toDataURL('image/jpeg', 0.95); // Aumentar qualidade
        capturedPhoto.src = dataURL;

        // Atualizar interface
        video.classList.add('d-none');
        photoPreview.classList.remove('d-none');
        btnCapturar.classList.add('d-none');
        btnNovaFoto.classList.remove('d-none');
        btnSalvarFoto.classList.remove('d-none');
        btnCompartilhar.classList.remove('d-none');

    } catch (error) {
        console.error('Erro ao capturar foto:', error);
        mostrarErroCamera('Erro ao capturar foto. Tente novamente.');
    }
}

